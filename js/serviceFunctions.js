function disableBorder(element) {
    element.style.border = 'none'; // Disable the border of the DOM element
};

function setOpacity(element, value) {
    element.style.opacity = `${value}`; // Change the opacity value of the DOM element
};

function setDisplay(element, value) {
    element.style.display = `${value}`; // Change the display value of the DOM element
};
